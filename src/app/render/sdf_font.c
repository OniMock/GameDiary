/**
 * @file sdf_font.c
 * @brief SDF font renderer — multi-page atlas implementation.
 *
 * The font pipeline (embed_generator.py) produces one C file per font group,
 * each exposing:
 *
 *   const EmbeddedAtlas  g_font_<group>_pages[];   // array of {png,bin} pairs
 *   const int            g_font_<group>_page_count; // number of pages
 *
 * This file flattens every page from every group into a single runtime array
 * of SDFAtlas structs, then performs glyph lookup across all of them in order.
 *
 * Fallback chain order (defined by g_group_descs[] below):
 *   latin_cyrillic → symbols → cjk_jp → cjk_sc
 */

#include "app/render/sdf_font.h"
#include "app/render/font_embed.h"
#include "app/i18n/i18n.h"
#include <pspkernel.h>
#include <pspgu.h>
#include <stdio.h>
#include <stdlib.h>
#include <malloc.h>   /* memalign() on PSP */
#include <string.h>
#include <stdint.h>
#include <png.h>
#include <math.h>

/* -----------------------------------------------------------------------
 * Binary file structures – must match exactly what font_generator.py writes
 * ----------------------------------------------------------------------- */
#pragma pack(push, 1)
typedef struct {
    char     magic[4];      /* "SDF\0" */
    uint32_t width;         /* atlas image width  (pixels) */
    uint32_t height;        /* atlas image height (pixels) */
    float    line_height;   /* em units */
    float    base_size;     /* emSize from JSON (== 32.0 for our pipeline) */
    uint32_t glyph_count;
} SDFHeader;

typedef struct {
    uint32_t codepoint;
    float u, v;   /* atlas top-left UV (0..1 normalised) */
    float w, h;   /* atlas rect size    (0..1 normalised) */
    float xoff;   /* planeBounds.left   (em units) */
    float yoff;   /* planeBounds.top    (em units, negative = above baseline) */
    float xadv;   /* horizontal advance (em units) */
} SDFGlyph;
#pragma pack(pop)

/* -----------------------------------------------------------------------
 * Runtime atlas handle (one per page)
 * ----------------------------------------------------------------------- */
typedef struct {
    SDFHeader header;
    uint16_t  lookup[65536]; /* O(1) codepoint -> glyph-array-index+1 */
    SDFGlyph *glyphs;
    /* Decoded RGBA8888 texture (memalign'd, PSP cache-flushed) */
    void     *tex_data;
    int       tex_w;        /* POT width  (for sceGuTexImage) */
    int       tex_h;        /* POT height */
    int       img_w;        /* actual image width  (informational) */
    int       img_h;        /* actual image height (informational) */
    int       group_id;     /* 0: Latin, 1: Symbols, 2: JP, 3: SC */
    int       loaded;
} SDFAtlas;

/* -----------------------------------------------------------------------
 * Vertex layout: GU_TEXTURE_32BITF | GU_COLOR_8888 | GU_VERTEX_32BITF
 * ----------------------------------------------------------------------- */
typedef struct {
    float    u, v;
    uint32_t color;
    float    x, y, z;
} VertexSDF;

/* -----------------------------------------------------------------------
 * Group descriptor — maps the static arrays generated by embed_generator.py
 * to runtime entries.  Order here defines the fallback search chain.
 * ----------------------------------------------------------------------- */
typedef struct {
    const EmbeddedAtlas *pages;
    int                  page_count;
} GroupDesc;

/* Populate from the extern arrays declared in font_embed.h */
static GroupDesc g_group_descs[] = {
    { g_font_latin_cyrillic_pages, 0 },   /* will be filled at init */
    { g_font_symbols_pages,        0 },
    { g_font_cjk_jp_pages,         0 },
    { g_font_cjk_sc_pages,         0 },
};
#define NUM_GROUPS ((int)(sizeof(g_group_descs) / sizeof(g_group_descs[0])))

/* -----------------------------------------------------------------------
 * Flat atlas registry — all pages from all groups concatenated.
 * Max: 2 + 1 + 8 + 8 = 19 pages (generous upper bound)
 * ----------------------------------------------------------------------- */
#define MAX_ATLASES 32
static SDFAtlas g_atlases[MAX_ATLASES];
static int      g_atlas_count = 0;

/* Global O(1) lookup table: codepoint -> atlas_index (0xFF = missing) */
static uint8_t g_glyph_map[65536];

/* -----------------------------------------------------------------------
 * PNG memory reader (libpng callback to read from an in-memory buffer)
 * ----------------------------------------------------------------------- */
typedef struct {
    const unsigned char *data;
    unsigned int         size;
    unsigned int         pos;
} PngMemSrc;

static void png_mem_read(png_structp png, png_bytep out, png_size_t len) {
    PngMemSrc *src = (PngMemSrc *)png_get_io_ptr(png);
    if (src->pos + len > src->size) {
        png_error(png, "PNG memory read overflow");
        return;
    }
    memcpy(out, src->data + src->pos, len);
    src->pos += len;
}

/* Round up to next power of two (PSP texture requirement) */
static int next_pow2(int n) {
    int x = 1;
    while (x < n) x <<= 1;
    return x;
}

/* -----------------------------------------------------------------------
 * Decode a PNG from a byte buffer into a 16-byte-aligned RGBA8888 buffer.
 *
 * The SDF distance lives in the Red channel.  We repack it as:
 *   alpha = R (distance value)
 *   RGB   = 0xFFFFFF  (modulated by vertex color at draw time)
 *
 * This allows sceGu to use GU_TFX_MODULATE so the vertex color tints glyphs.
 * ----------------------------------------------------------------------- */
static void *decode_png(const unsigned char *png_data, unsigned int png_size,
                        int *out_w, int *out_h, int *out_tw, int *out_th)
{
    PngMemSrc mem_src = { png_data, png_size, 0 };

    png_structp png = png_create_read_struct(PNG_LIBPNG_VER_STRING,
                                              NULL, NULL, NULL);
    if (!png) return NULL;

    png_infop info = png_create_info_struct(png);
    if (!info) { png_destroy_read_struct(&png, NULL, NULL); return NULL; }

    if (setjmp(png_jmpbuf(png))) {
        png_destroy_read_struct(&png, &info, NULL);
        return NULL;
    }

    png_set_read_fn(png, &mem_src, png_mem_read);
    png_read_png(png, info,
                 PNG_TRANSFORM_STRIP_16 | PNG_TRANSFORM_PACKING |
                 PNG_TRANSFORM_EXPAND, NULL);

    int w        = (int)png_get_image_width(png, info);
    int h        = (int)png_get_image_height(png, info);
    int channels = (int)png_get_channels(png, info);
    int tw       = next_pow2(w);
    int th       = next_pow2(h);

    /* Allocate with 16-byte alignment for DMA / GE safety */
    uint32_t *buf = (uint32_t *)memalign(16, (unsigned int)(tw * th) * 4u);
    if (!buf) {
        png_destroy_read_struct(&png, &info, NULL);
        return NULL;
    }
    memset(buf, 0, (unsigned int)(tw * th) * 4u);

    png_bytep *rows = png_get_rows(png, info);
    for (int y = 0; y < h; y++) {
        for (int x = 0; x < w; x++) {
            png_bytep p = &rows[y][x * channels];
            uint8_t  r = p[0];
            /*
             * SDF distance is stored in the Red channel.
             * Format: AABBGGRR in PSP memory (little-endian RGBA in struct).
             * We want: alpha = distance, RGB = 0xFFFFFF.
             */
            buf[y * tw + x] = ((uint32_t)r << 24) | 0x00FFFFFFu;
        }
    }

    png_destroy_read_struct(&png, &info, NULL);

    *out_w = w; *out_h = h; *out_tw = tw; *out_th = th;
    sceKernelDcacheWritebackAll();
    return buf;
}

/* -----------------------------------------------------------------------
 * Load one atlas page from an EmbeddedAtlas record into the registry.
 * ----------------------------------------------------------------------- */
static int load_atlas_from_embed(int slot, const EmbeddedAtlas *embed, int group_id) {
    SDFAtlas *a = &g_atlases[slot];
    memset(a, 0, sizeof(SDFAtlas));
    a->group_id = group_id;

    /* --- PNG → RGBA buffer --- */
    a->tex_data = decode_png(
        embed->png_data, embed->png_size,
        &a->img_w, &a->img_h, &a->tex_w, &a->tex_h);

    if (!a->tex_data) {
        printf("sdf_font: atlas[%d] PNG decode failed\n", slot);
        return -1;
    }

    /* --- BIN: header + 65536-entry lookup + glyph array --- */
    const unsigned char *bin = embed->bin_data;
    unsigned int         sz  = embed->bin_size;
    unsigned int         off = 0;

    if (sz < sizeof(SDFHeader) + sizeof(uint16_t) * 65536) {
        printf("sdf_font: atlas[%d] BIN too small (%u bytes)\n", slot, sz);
        free(a->tex_data);
        return -1;
    }

    memcpy(&a->header, bin + off, sizeof(SDFHeader));
    off += sizeof(SDFHeader);

    if (strncmp(a->header.magic, "SDF", 3) != 0) {
        printf("sdf_font: atlas[%d] bad magic\n", slot);
        free(a->tex_data);
        return -1;
    }

    memcpy(a->lookup, bin + off, sizeof(uint16_t) * 65536);
    off += sizeof(uint16_t) * 65536;

    unsigned int glyph_bytes = a->header.glyph_count * sizeof(SDFGlyph);
    if (off + glyph_bytes > sz) {
        printf("sdf_font: atlas[%d] glyph data truncated\n", slot);
        free(a->tex_data);
        return -1;
    }

    a->glyphs = (SDFGlyph *)malloc(glyph_bytes);
    if (!a->glyphs) {
        free(a->tex_data);
        return -1;
    }
    memcpy(a->glyphs, bin + off, glyph_bytes);

    a->loaded = 1;
    return 0;
}

